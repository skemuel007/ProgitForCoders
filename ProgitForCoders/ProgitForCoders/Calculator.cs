using System;
namespace ProgitForCoders
{
	public class Calculator
	{
		public Calculator()
		{
		}

		public int Add(int left, int right)
		{
			return left + right;
		}

		public int Subtract(int left, int right)
		{
			return left - right;
		}

		public int Multiply(int left, int right)
		{
			return left * right;
		}
	}
}

